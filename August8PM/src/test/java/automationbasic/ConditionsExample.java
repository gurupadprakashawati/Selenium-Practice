public class ConditionsExample {
    public static void main(String[] args) {
        int a = 20;
        int b = 15;
        System.out.println(a);
        if (a == b){
            System.out.println("Equal");
        } else if (a>b) {
            System.out.println("a is greater");

        } else{
            System.out.println("Not Equal");
        }

    }

}
